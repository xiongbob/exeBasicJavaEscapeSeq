

public class EscapeSeq {
    public static void main (String [] args) {
        System.out.println ("Print the following columns of values. Use \\t for the tabs between the columns.");

        System.out.print ("Cost\t      Quantity\t      Total\t\n");
        System.out.print ("$1,000.00\t   4\t       $4,000.00\t\n");
        System.out.print ("$   50.00\t   8\t         $400.00\t\n");
        System.out.print ("-----------------------------------\n");
        System.out.print ("TOTAL:\t      12\t      $4,400.00\t\n");
    }
}
